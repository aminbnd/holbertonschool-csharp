using System;

namespace MyMath
{
    /// <summary>
    /// Math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Function that returns the sum of two integers
        /// </summary>
        /// <param name="a">first integer</param>
        /// <param name="b">second integer</param>
        /// <returns></returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
